class FinancialProperties {
    constructor(company,
                year,
                quarter,
                url,
                net_revenue,
                adv_revenue,
                other_service_revenue,
                net_profit,
                mua,
                dua) {
        this.company = company;
        this.year = year;
        this.quarter = quarter;
        this.url = url;
        this.net_revenue = net_revenue;
        this.adv_revenue = adv_revenue;
        this.other_service_revenue = other_service_revenue;
        this.net_profit = net_profit;
        this.mua = mua;
        this.dua = dua;
    }
}